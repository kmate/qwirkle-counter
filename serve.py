#!/usr/bin/env python3
"""
Simple HTTP server for testing the Qwirkle Counter app locally.
Run this script and access the app at http://localhost:8000
"""

import http.server
import socketserver
import os

PORT = 8000

class MyHTTPRequestHandler(http.server.SimpleHTTPRequestHandler):
    def end_headers(self):
        # Enable CORS for local testing
        self.send_header('Access-Control-Allow-Origin', '*')
        self.send_header('Access-Control-Allow-Methods', 'GET, POST, OPTIONS')
        self.send_header('Access-Control-Allow-Headers', 'Content-Type')
        # Required for camera access
        self.send_header('Permissions-Policy', 'camera=(self)')
        super().end_headers()

def main():
    os.chdir(os.path.dirname(os.path.abspath(__file__)))
    
    with socketserver.TCPServer(("", PORT), MyHTTPRequestHandler) as httpd:
        print(f"Server running at http://localhost:{PORT}/")
        print(f"Access on mobile: http://{get_local_ip()}:{PORT}/")
        print("Press Ctrl+C to stop")
        print("\nNote: For camera access on mobile, you may need HTTPS.")
        print("Consider using ngrok or similar for remote testing.")
        httpd.serve_forever()

def get_local_ip():
    import socket
    try:
        s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
        s.connect(("8.8.8.8", 80))
        ip = s.getsockname()[0]
        s.close()
        return ip
    except:
        return "localhost"

if __name__ == "__main__":
    main()
